/**
 * Функция умножения.
 * @param {number} a - Первое число. 
 * @param {number} b - Второе число.
 * @returns {number}
 */
function multiply (a, b) {
    return a * b;
}
multiply(50, 21);

/**
 * Функция деления.
 * @param {number} a - Первое число. 
 * @param {number} b - Второе число.
 * @returns {number}
 */
function divide (a, b) {
    return a / b;
}
divide(5, 0);